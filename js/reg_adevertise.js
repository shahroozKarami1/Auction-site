const exgra_pic_img = document.querySelector(".exgra_pic-img"),
  image = document.querySelectorAll(".pics_group .image"),
  image_img = document.querySelectorAll(".pics_group .image img"),
  image_input = document.querySelectorAll(".pics_group .image input"),
  image_span = document.querySelectorAll(".pics_group .image span"),
  image_i = document.querySelectorAll(".pics_group .image i");

const openInput = (jj) => {
  image_input[jj].click();
  image_input[jj].addEventListener("change", () => {
    const newFile = image_input[jj].files[0];
    const fileValidator = ["image/jpeg", "image/jpg", "image/png"];
    const fileType = newFile.type;
    if (newFile && fileValidator.includes(fileType)) {
      const newFileReader = new FileReader();
      newFileReader.addEventListener("load", () => {
        const newResult = newFileReader.result;
        image_img[jj].src = newResult;
        image[jj].classList.add("hide_show");
        exgra_pic_img.src = newResult;
      });
      newFileReader.readAsDataURL(newFile);
    } else {
      alert("باشد jpg,jpeg,png فرمت عکس باید ");
    }
  });
};

image_i.forEach((i, j) => {
  i.onclick = () => {
    openInput(j);
  };
});

image_span.forEach((k, m) => {
  k.onclick = () => {
    image[m].classList.remove("hide_show");
    image_img[m].src = "";
    exgra_pic_img.src = "";
  };
});

